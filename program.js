var num=prompt("Please enter a number in between 0-6: ")

if(num==0){
    console.log("SUNDAY");
}
else if (num==1){
    console.log("MONDAY");
}
else if (num==2){
    console.log("TUESDAY");
}
else if (num==3){
    console.log("WEDNESSDAY");
}
else if (num==4){
    console.log("THURSEDAY");
}
else if (num==5){
    console.log("FRIDAY");
}
else if (num==6){
    console.log("SATURDAY");
}
else{
    console.log("Please enter number in between 0 to 6 ");
}